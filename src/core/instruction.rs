use strum::Display;

pub struct Instruction {
  pub opcode: OpCode,
  pub mode: AddressingMode,
  pub cycles: u8,
}

#[derive(Debug, Display)]
pub enum AddressingMode {
  Accumulator,
  Implied,
  Immediate(u8),
  Relative(u8),

  ZeroPage(u8),
  ZeroPageX(u16),
  ZeroPageY(u16),

  Absolute(u16),
  AbsoluteX(u16),
  AbsoluteY(u16),

  Indirect(u16),
  IndirectX(u16),
  IndirectY(u16),
}

// Also imports and uses the individual instructions
pub enum OpCode {
  ADC,
  AND,
  ASL,
  BCC,
  BCS,
  BEQ,
  BIT,
  BMI,
  BNE,
  BPL,
  BRK,
  BVC,
  BVS,
  CLC,
  CLD,
  CLI,
  CLV,
  CMP,
  CPX,
  CPY,
  DEC,
  DEX,
  DEY,
  EOR,
  INC,
  INX,
  INY,
  JMP,
  JSR,
  LDA,
  LDX,
  LDY,
  LSR,
  NOP,
  ORA,
  PHA,
  PHP,
  PLA,
  PLP,
  ROL,
  ROR,
  RTI,
  RTS,
  SBC,
  SEC,
  SED,
  SEI,
  STA,
  STX,
  STY,
  TAX,
  TAY,
  TSX,
  TXA,
  TXS,
  TYA,
}
